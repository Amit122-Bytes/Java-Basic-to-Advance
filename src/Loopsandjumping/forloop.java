package Loopsandjumping;

public class forloop {

	public static void main(String[] args) {
		
		for(int i=1;i<=10;i++)//Low--->high number
		{
			System.out.println(i);
		}
		
		System.out.println("------------------------------------------");
		
		for(int i=10;i>=1;i--)//High--->low number
		{
			System.out.println(i);
		}

	}

}
